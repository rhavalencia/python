#Typecasting = converting one data type to another data type

name = "Rhav"
age = 30
gpa = 3.8
is_student = False

#Checking the type of variables
print(type(name))
print(type(age))
print(type(is_student))

#Typecasting
#Float to Integer
gpa = int(gpa) 
print(gpa)

#Integer to Float
ageToFloat = float(age)
print(ageToFloat)

#Integer to String
ageToString = str(age)
print(ageToString)  